let currentLang = localStorage.getItem('polyedges-lang') || 'ca';

export function t(key, params = {}) {
  let str = translations[currentLang]?.[key]
    || translations['ca'][key]
    || key;
  for (const [k, v] of Object.entries(params)) {
    str = str.replaceAll(`{{${k}}}`, v);
  }
  return str;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('polyedges-lang', lang);
  document.documentElement.lang = lang;
  applyTranslations();
  window.dispatchEvent(new Event('langchange'));
}

export function getLang() {
  return currentLang;
}

export function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else if (el.tagName === 'OPTGROUP') {
      el.label = t(key);
    } else if (el.tagName === 'OPTION' && el.dataset.info) {
      el.textContent = `${t(key)} (${el.dataset.info})`;
    } else {
      el.textContent = t(key);
    }
  });
}

